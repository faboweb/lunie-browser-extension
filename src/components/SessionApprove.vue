<template>
  <div class="session-approve">
    <h2>Approve Transaction</h2>
    <br />
    <div class="from">
      From
      <Address :address="senderAddress" />
    </div>
    <TmFormGroup v-if="signRequest">
      <TransactionItem
        v-if="tx"
        :key="tx.hash"
        :transaction="tx"
        :validators="validatorsAddressMap"
        :address="senderAddress"
        :show-meta-data="false"
      />
      <!-- Going to take some more logic based on how transactions are passed in -->
      <TableInvoice
        class="approval-table"
        :amount="amount"
        :estimated-fee="fees"
        :bond-denom="bondDenom"
      />
      <TmFormGroup
        :error="$v.password.$error && $v.password.$invalid"
        class="action-modal-group"
        field-id="password"
        field-label="Password"
      >
        <TmField
          id="password"
          v-model="password"
          v-focus
          type="password"
          placeholder="Password"
        />
        <TmFormMsg
          v-if="$v.password.$error && !$v.password.required"
          name="Password"
          type="required"
        />
        <TmFormMsg
          v-if="$v.password.$error && !$v.password.passwordCorrect"
          name="Password"
          type="custom"
          msg="is incorrect"
        />
      </TmFormGroup>

      <div class="session-approve-footer">
        <TmBtn
          id="reject-btn"
          value="Reject"
          class="left-button"
          type="secondary"
          @click.native="reject"
        />
        <TmBtn
          id="approve-btn"
          value="Approve"
          class="right-button"
          type="primary"
          @click.native="approve"
        />
      </div>
    </TmFormGroup>
  </div>
</template>

<script>
import { mapGetters } from 'vuex'
import TmBtn from 'common/TmBtn'
import TmFormGroup from 'common/TmFormGroup'
import TmField from 'common/TmField'
import TmFormMsg from 'common/TmFormMsg'
import TransactionItem from 'transactions/TransactionItem'
import TableInvoice from 'src/ActionModal/components/TableInvoice'
import Address from 'common/Address'
import { required } from 'vuelidate/lib/validators'
import actions from '../store/actions.js'

const getValidatorsData = actions({}).getValidatorsData
const parseSignMessageTx = actions({}).parseSignMessageTx

export default {
  name: `session-approve`,
  components: {
    TmBtn,
    TmFormGroup,
    TransactionItem,
    TableInvoice,
    Address,
    TmField,
    TmFormMsg
  },
  data: () => ({
    validators: [],
    password: null,
    passwordError: false
  }),
  computed: {
    ...mapGetters(['signRequest', 'networks']),
    tx() {
      // const network = this.networks.find(
      //   ({ id }) => id === this.signRequest.network.id
      // )
      return parseSignMessageTx(
        this.signRequest.network,
        this.signRequest.lunieTransaction
      )
    },
    network() {
      return this.signRequest ? this.signRequest.network : null
    },
    fees() {
      return this.tx ? this.tx.fees[0].amount : null
    },
    senderAddress() {
      return this.signRequest ? this.signRequest.senderAddress : null
    },
    amountCoin() {
      return this.tx ? this.tx.details.amount : null
    },
    amount() {
      return this.amountCoin ? this.amountCoin.amount : 0
    },
    bondDenom() {
      return this.amountCoin ? this.amountCoin.denom : ''
    },
    validatorsAddressMap() {
      const names = {}
      this.validators.forEach(item => {
        names[item.operatorAddress] = item
      })
      return names
    }
  },
  watch: {
    password: function() {
      this.passwordError = false
    }
  },
  async mounted() {
    const validatorsObject = await getValidatorsData(this.tx, this.network)
    this.validators = validatorsObject
  },
  methods: {
    isValidInput(property) {
      this.$v[property].$touch()

      return !this.$v[property].$invalid
    },
    async approve() {
      if (this.isValidInput('password')) {
        await this.$store
          .dispatch('approveSignRequest', {
            ...this.signRequest,
            password: this.password
          })
          .catch(e => {
            if (e === 'Incorrect password') {
              this.passwordError = true
            }
            return
          })
        this.$router.push(`/success`)
      }
    },
    async reject() {
      await this.$store.dispatch('rejectSignRequest', {
        ...this.signRequest
      })
      window.close()
    },
    correctPassword() {
      return !this.passwordError
    }
  },
  validations() {
    const passwordCorrect = this.correctPassword
    return {
      password: {
        required,
        passwordCorrect
      }
    }
  }
}
</script>

